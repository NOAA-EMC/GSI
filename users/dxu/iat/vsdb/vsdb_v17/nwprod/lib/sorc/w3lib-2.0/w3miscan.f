                     IF((IT.NE.2.AND.KDATA(IT).LT.10000).OR.
     $                  (IT.EQ.2.AND.KDATA(IT).LT. 8000))  THEN
                        LBTER(IT) = LBTER(IT) + 1
                       PRINT 779,IT,IBUFTN(8),IBUFTN(9),KDATA
  779 FORMAT(' ##W3MISCAN: BT, CHN',I2,' BAD: SCAN',I6,', ORBIT',I8,
     $ '; BT:',7I6,'-CANNOT CALC. PRODS VIA ALG.')
                        GO TO 112
                     END IF
