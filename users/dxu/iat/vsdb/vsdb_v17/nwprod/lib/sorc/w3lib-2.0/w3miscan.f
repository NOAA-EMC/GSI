            IF(NINT(PROD(10,IRT)).LT.IMSG)
     $       IBUFTN((27*IRT)-04) = NINT(PROD(10,IRT)*1000.)

C STORE THE RAIN FLAG (0-3) IF AVAILABLE

            IBUFTN((27*IRT)-03) = MIN(IMSG,NINT(PROD(11,IRT)))

