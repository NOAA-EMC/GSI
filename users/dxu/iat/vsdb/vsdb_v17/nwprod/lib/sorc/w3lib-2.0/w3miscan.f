      IF(IRET.NE.0)  THEN

C ALL SUBSETS OUT OF THIS MESSAGE READ, TIME TO MOVE ON TO NEXT MESSAGE

         CALL READMG(INDTA,SUBSET,IBDATE,IRET)

         print *, 'READ NEXT BUFR MESSAGE: SUBSET = ',SUBSET,
