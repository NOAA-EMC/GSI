            END DO
            DO JJ = 239,249
               IF(KSPSAT(JJ).GT.0)  THEN
                  II = JJ
                  IF(JJ.EQ.239)  II = 1
                  PRINT  224, II,KSPSAT(JJ) 
  224             FORMAT(35X,'NO. OF SCANS SKIPPED DUE TO BEING FROM ',
