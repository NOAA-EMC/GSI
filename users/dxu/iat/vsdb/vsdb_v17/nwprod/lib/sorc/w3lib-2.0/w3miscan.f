                     GO TO 112
                  END IF

C      ..... REJECT IF OVER ICE (USE SEA-SURFACE TEMPERATURE HERE)

                  IF(SSTDAT(IX,JY).LE.272.96)  THEN
                     NIR = NIR + 1
                     GO TO 112
                  END IF

                  KDATA = IBUFTN((27*IRT)-01:(27*IRT)+05)
